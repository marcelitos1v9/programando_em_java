    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed
        ven.setCodigo(Integer.parseInt(txt_codigo.getText()));
        ven.setNomeVendedor(txt_nome.getText());
        ven.setProdutoVendido(txt_produto.getText());

        ven.cadastrarVenda();
        consultarTodasVendas();
    }//GEN-LAST:event_btn_cadastrarActionPerformed

    private void btn_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alterarActionPerformed
        int linhaSelecionada = tbl_vendas.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma venda para alterar.");
            return;
        }

        int codigoAtual = Integer.parseInt((String) tbl_vendas.getValueAt(linhaSelecionada, 0));

        String novoCodigoStr = txt_codigo.getText();
        int novoCodigo = Integer.parseInt(novoCodigoStr);
        String novoNomeVendedor = txt_nome.getText();
        String novoProdutoVendido = txt_produto.getText();

        ven.alterarVenda(codigoAtual, novoCodigo, novoNomeVendedor, novoProdutoVendido);

        DefaultTableModel modeloTabela = (DefaultTableModel) tbl_vendas.getModel();
        modeloTabela.setRowCount(0);
        consultarTodasVendas();
    }//GEN-LAST:event_btn_alterarActionPerformed

    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
                                           
    // Verifica se uma linha foi selecionada na tabela de vendas
    int linhaSelecionada = tbl_vendas.getSelectedRow();
    if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(null, "Selecione uma venda para excluir.");
        return;
    }

    // ObtÃ©m o cÃ³digo da venda da linha selecionada
    int codigoVenda = Integer.parseInt((String) tbl_vendas.getValueAt(linhaSelecionada, 0));

    // ConfirmaÃ§Ã£o da exclusÃ£o
    int confirmacao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir esta venda?", "ConfirmaÃ§Ã£o de ExclusÃ£o", JOptionPane.YES_NO_OPTION);
    if (confirmacao == JOptionPane.YES_OPTION) {
        // Chama o mÃ©todo para excluir a venda
        ven.excluirVenda(codigoVenda);

        // Atualiza a tabela de vendas apÃ³s a exclusÃ£o
        DefaultTableModel modeloTabela = (DefaultTableModel) tbl_vendas.getModel();
        modeloTabela.setRowCount(0); // Limpa a tabela antes de popular com os novos dados
        consultarTodasVendas(); // Preenche a tabela novamente com os dados atualizados
    }


    }//GEN-LAST:event_btn_excluirActionPerformed
    private void consultarTodasVendas() {
        DefaultTableModel modeloTabela = (DefaultTableModel) tbl_vendas.getModel();
        modeloTabela.setRowCount(0);
        ven.consultarVenda(modeloTabela);
    }

